def get_mermaid_type(column_type):
    type_str = str(column_type).upper()
    if 'VARCHAR' in type_str or 'TEXT' in type_str or 'STRING' in type_str or 'CHAR' in type_str:
        return 'string'
    elif 'INT' in type_str:
        return 'int'
    elif 'FLOAT' in type_str or 'DECIMAL' in type_str or 'NUMERIC' in type_str:
        return 'float'
    elif 'BOOL' in type_str:
        return 'boolean'
    elif 'TIMESTAMP' in type_str or 'DATETIME' in type_str:
        return 'datetime'
    elif 'DATE' in type_str:
        return 'date'
    elif 'JSON' in type_str:
        return 'json'
    else:
        return type_str.split('(')[0]

def generate_schema_markdown():
    tables = Base.metadata.tables
    
    lines = []
    lines.append("# Database Schema")
    lines.append("")
    lines.append("This document is auto-generated by `backend/scripts/generate_schema_doc.py` whenever a database migration is run.")
    lines.append("")
    lines.append("## Entity-Relationship Diagram")
    lines.append("")
    lines.append("```mermaid")
    lines.append("erDiagram")
    
    # 1. Add Tables and Columns
    for table_name, table in tables.items():
        lines.append(f"    {table_name} {{")
        for column in table.columns:
            col_type = get_mermaid_type(column.type)
            constraints = []
            if column.primary_key:
                constraints.append("PK")
            elif column.foreign_keys:
                constraints.append("FK")
            elif column.unique:
                constraints.append("UK")
            
            constraint_str = " ".join(constraints) if constraints else ""
            # Mermaid doesn't like spaces in type names, so strip them
            safe_type = col_type.replace(" ", "_").replace("(", "").replace(")", "").replace("[", "").replace("]", "")
            if not safe_type:
                safe_type = "unknown"
            
            # Use quotes if column name conflicts with mermaid keywords, but simple names are fine
            col_name = column.name
            lines.append(f"        {safe_type} {col_name} {constraint_str}")
        lines.append("    }")
    
    lines.append("")
    
    # 2. Add Relationships
    for table_name, table in tables.items():
        for column in table.columns:
            for fk in column.foreign_keys:
                target_table = fk.column.table.name
                # Many-to-one since foreign key is on the many side
                lines.append(f"    {table_name} }}|--|| {target_table} : \"{column.name}\"")
                
    lines.append("```")
    lines.append("")
    
    # 3. Add Indices Section
    lines.append("## Indices & Constraints")
    lines.append("")
    for table_name, table in tables.items():
        has_indices = len(table.indexes) > 0 or any(c.unique and not c.primary_key for c in table.columns)
        if has_indices:
            lines.append(f"### `{table_name}`")
            for idx in table.indexes:
                cols = ", ".join([c.name for c in idx.columns])
                lines.append(f"- **Index**: `{idx.name}` on columns: ({cols})")
            for c in table.columns:
                if c.unique and not c.primary_key:
                    lines.append(f"- **Unique**: `{c.name}`")
            lines.append("")

    return "\n".join(lines)

