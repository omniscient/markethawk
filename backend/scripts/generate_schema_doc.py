def main():
    docs_dir = os.path.abspath(os.path.join(os.path.dirname(__file__), '..', '..', 'Docs'))
    os.makedirs(docs_dir, exist_ok=True)
    
    # Generate Markdown
    md_output_path = os.path.join(docs_dir, 'database-schema.md')
    markdown_content = generate_schema_markdown()
    
    with open(md_output_path, 'w', encoding='utf-8') as f:
        f.write(markdown_content)
        
    # Generate HTML
    html_output_path = os.path.join(docs_dir, 'database-schema.html')
    # Extract just the mermaid block for the HTML
    mermaid_block = ""
    in_mermaid = False
    for line in markdown_content.split('\n'):
        if line.startswith("```mermaid"):
            in_mermaid = True
            continue
        elif line.startswith("```") and in_mermaid:
            in_mermaid = False
            continue
        
        if in_mermaid:
            mermaid_block += line + "\n"
            
    html_content = generate_schema_html(mermaid_block)
    
    with open(html_output_path, 'w', encoding='utf-8') as f:
        f.write(html_content)
        
    print(f"Successfully generated database schema document at {md_output_path} and {html_output_path}")

if __name__ == "__main__":
    main()
